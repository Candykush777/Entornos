package Branch_Projects;

public class metodos {

    public int sumar (int a, int b){

         return a + b;
    }

    public  int restar (int a, int b){
         return  a - b;
    }

    public int division (int a, int b){
        return a / b;
    }

    public int multiplicacion (int a, int b){
        return  a * b;
    }


}
